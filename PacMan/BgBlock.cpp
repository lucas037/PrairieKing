BgBlock::BgBlock(float x, float y, string name)
{
	sprite = new Sprite("Resources/"+name+".png");

	MoveTo(x, y);
	
	if (name == "bgWall") {
		BBox(new Rect(-24, -24, 24, 24));
		type = BUSH;
	}
}

